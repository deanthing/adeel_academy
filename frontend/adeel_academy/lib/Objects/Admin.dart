class Admin {
  // Instance Variables
  String userID;
  String name;
  String email;

  /**
   * Getters and Setters for the Following Variables Above
   */

  String getUserID() {
    return userID;
  }

  void setUserID(String newUser) {
    this.userID = newUser;
  }

  String getName() {
    return name;
  }

  void setName(String newName) {
    this.name = newName;
  }

  String getEmail() {
    return email;
  }

  void setEmail(String newEmail) {
    this.email = newEmail;
  }
}
